import streamlit as st
import pandas as pd
import joblib
import matplotlib.pyplot as plt
import seaborn as sns
from fpdf import FPDF

# ==========================================
# 1. UPDATED PDF Generation Function (With Medical Values)
# ==========================================
def create_pdf(name, age, gender, result, prob, recs, medical_data):
    pdf = FPDF()
    pdf.add_page()
    
    # Header
    pdf.set_font("Arial", 'B', 20)
    pdf.cell(200, 15, txt="Chronic Disease Health Assessment Report", ln=True, align='C')
    pdf.ln(5)
    pdf.set_font("Arial", 'I', 10)
    pdf.cell(200, 10, txt="Generated by AI Diagnostic System", ln=True, align='C')
    pdf.ln(10)
    
    # 1. Patient Profile
    pdf.set_fill_color(230, 230, 230)
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, txt=" PATIENT PROFILE", ln=True, fill=True)
    pdf.set_font("Arial", size=11)
    pdf.cell(100, 10, txt=f"Name: {name}", border=0)
    pdf.cell(100, 10, txt=f"Age: {age}", ln=True)
    pdf.cell(100, 10, txt=f"Gender: {gender}", border=0)
    pdf.cell(100, 10, txt=f"Status: {result}", ln=True)
    pdf.ln(5)

    # 2. Medical Metrics Table (Specific Values)
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, txt=" CLINICAL MEASUREMENTS", ln=True, fill=True)
    pdf.set_font("Arial", 'B', 10)
    
    # Table Header
    pdf.cell(95, 10, "Parameter", border=1, align='C')
    pdf.cell(95, 10, "Patient Value", border=1, align='C', ln=True)
    
    pdf.set_font("Arial", size=10)
    for key, value in medical_data.items():
        pdf.cell(95, 10, f" {key}", border=1)
        pdf.cell(95, 10, f" {value}", border=1, ln=True)
    pdf.ln(5)

    # 3. Risk Assessment
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, txt=" RISK ANALYSIS", ln=True, fill=True)
    pdf.set_font("Arial", size=11)
    pdf.cell(0, 10, txt=f"Calculated Disease Probability: {prob:.1f}%", ln=True)
    pdf.ln(5)

    # 4. Recommendations
    pdf.set_font("Arial", 'B', 12)
    pdf.cell(0, 10, txt=" DOCTOR'S RECOMMENDATIONS", ln=True, fill=True)
    pdf.set_font("Arial", size=10)
    for r in recs:
        pdf.multi_cell(0, 8, txt=r)
    
    return pdf.output(dest='S').encode('latin-1')

# ==========================================
# 2. App Logic
# ==========================================
st.set_page_config(page_title="Chronic Disease AI", layout="wide")

try:
    pipeline = joblib.load('full_pipeline_compressed.sav')
except:
    st.error("Model file not found!")

st.title("🏥 Professional Health Prediction & Reporting")

col1, col2 = st.columns(2)

with col1:
    st.subheader("Patient Basics")
    patient_name = st.text_input("Full Name", "Patient Name")
    age = st.number_input("Age", 1, 120, 30)
    gender = st.selectbox("Gender", ["Male", "Female", "Other"])
    bmi = st.number_input("BMI (kg/m2)", 10.0, 50.0, 25.0)
    smoking = st.selectbox("Smoking Habit", ["No", "Yes"])
    alcohol = st.selectbox("Alcohol Intake", ["Low", "Moderate", "High"])
    activity = st.slider("Activity (Hrs/Week)", 0.0, 10.0, 3.0)

with col2:
    st.subheader("Clinical Metrics")
    diet = st.selectbox("Diet Quality", ["Poor", "Average", "Good"])
    sleep = st.slider("Sleep Hours", 0.0, 12.0, 7.0)
    bp = st.number_input("Blood Pressure (Systolic)", 80, 200, 120)
    cholesterol = st.number_input("Cholesterol (mg/dL)", 100, 400, 200)
    glucose = st.number_input("Glucose Level (mg/dL)", 50, 300, 100)
    family_hist = st.selectbox("Family History", ["No", "Yes"])
    stress = st.slider("Stress Level (1-10)", 1, 10, 5)

# Storing clinical data for the PDF table
clinical_summary = {
    "Body Mass Index (BMI)": f"{bmi:.1f}",
    "Blood Pressure (Systolic)": f"{bp} mmHg",
    "Cholesterol Level": f"{cholesterol} mg/dL",
    "Glucose Level": f"{glucose} mg/dL",
    "Stress Level": f"{stress}/10",
    "Sleep Duration": f"{sleep} Hrs",
    "Physical Activity": f"{activity} Hrs/Week"
}

features = ['Age', 'Gender', 'BMI', 'Smoking', 'AlcoholIntake', 'PhysicalActivity', 'DietQuality', 'SleepHours', 'BloodPressure', 'Cholesterol', 'Glucose', 'FamilyHistory', 'StressLevel']
input_data = pd.DataFrame([[age, gender, bmi, smoking, alcohol, activity, diet, sleep, bp, cholesterol, glucose, family_hist, stress]], columns=features)

if st.button("Generate Diagnostic Report"):
    prediction = pipeline.predict(input_data)[0]
    prob = pipeline.predict_proba(input_data)[0][1] * 100
    res_text = "Chronic Disease Detected" if prediction == 1 else "No Chronic Disease"

    st.markdown("---")
    if prediction == 1:
        st.error(f"### Diagnosis: {res_text} (Risk: {prob:.1f}%)")
    else:
        st.success(f"### Diagnosis: {res_text} (Risk: {prob:.1f}%)")

    # Recommendations
    recs = []
    if bmi > 25: recs.append("- Overweight: Follow a low-calorie diet.")
    if glucose > 140: recs.append("- High Glucose: Immediate sugar restriction advised.")
    if bp > 130: recs.append("- Hypertension: Reduce salt and monitor BP daily.")
    if activity < 3: recs.append("- Inactivity: Regular physical exercise recommended.")
    
    # PDF Download with Actual Values
    pdf_file = create_pdf(patient_name, age, gender, res_text, prob, recs, clinical_summary)
    st.download_button(label="📥 Download Detailed PDF Report",
                       data=pdf_file,
                       file_name=f"Medical_Report_{patient_name}.pdf",
                       mime="application/pdf")
